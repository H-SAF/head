#!/bin/bash -e

#-----------------------------------------------------------------------------------------
# Script information
script_name='HEAD CLEANER - DEPRECATED SUBFOLDER(S)'
script_version="1.1.0"
script_date='2024/06/10'
#-----------------------------------------------------------------------------------------

#-----------------------------------------------------------------------------------------
# Get file information
main_folder='/share/DEWETRA/'
#-----------------------------------------------------------------------------------------

# ----------------------------------------------------------------------------------------
# Info script start
echo " ==================================================================================="
echo " ==> "$script_name" (Version: "$script_version" Release_Date: "$script_date")"
echo " ==> START ..."

find $main_folder -depth -empty -type d -exec rmdir {} \;

# Info script end
echo " ==> "$script_name" (Version: "$script_version" Release_Date: "$script_date")"
echo " ==> ... END"
echo " ==> Bye, Bye"
echo " ==================================================================================="
# ----------------------------------------------------------------------------------------
